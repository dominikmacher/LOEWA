<p>Diese Applikation soll Personen helfen Löschwasser-Berechnungen schneller und effizienter durchführen zu können.</p>

<p>Version: 2.0</p>

Aktuelle Features:
<ul>
    <li>Berechnen von Löschwasser-Objektschutz (inkl. Hinterlegung der spezifischen Löschwasserrate je Brandbelastung)</li>
    <li>Berechnen von Erster- und erweiterter Löschhilfe für Objekte</li>
    <li>Abspeichern von Objekt-Berechnungen</li>
    <li>Druck von Objekt-Berechnungen in speziellem auf A4-zugeschnittenes Druckformat</li>
</ul>

